#!/bin/bash

#-------------------------------------------------------------
# 变量单引号、双引号区别
#-------------------------------------------------------------
name=day1
echo '$name'
echo "$name"

#-------------------------------------------------------------
# 获取变量方式 $ || ${}
#-------------------------------------------------------------
yourname='style'
echo $yourname
echo ${yourname}

#-------------------------------------------------------------
# 变量重写
#-------------------------------------------------------------
your_name='tom'
echo ${your_name}
your_name='alibaba'
echo ${your_name}

#-------------------------------------------------------------
# 设置变量只读属性
#-------------------------------------------------------------
myUrl='http://www.baidu.com'
readonly myUrl
myUrl='http://www.test.com'
echo ${myUrl}

#-------------------------------------------------------------
# 删除变量
#-------------------------------------------------------------
deleteBianLiang='shan chu bian liang'
unset deleteBianLiang
echo ${deleteBianLiang}

#-------------------------------------------------------------
#  字符串长度
#-------------------------------------------------------------
string='abcd'
echo ${#string}

#-------------------------------------------------------------
# 提取子字符串
#-------------------------------------------------------------
substr="runoob is a great site"
echo ${substr:1:4}

#-------------------------------------------------------------
# 查找子字符串
#-------------------------------------------------------------
searchString="runoob is a great site"
echo `expr index "$searchString" io`

